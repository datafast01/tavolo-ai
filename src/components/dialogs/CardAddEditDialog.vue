<script setup>
import {
  alphaValidator,
  expiredateValidator,
  lengthValidator,
  requiredValidator,
} from "@validators";

const isNameValid = (value) => {
  return (
    lengthValidator(value, 1, 15) ||
    "Contract title must be between 1 and 15 characters"
  );
};

const props = defineProps({
  cardDetails: {
    type: Object,
    required: false,
    default: () => ({
      number: "",
      name: "",
      expiry: "",
      cvv: "",
      isPrimary: false,
      type: "",
    }),
  },
  isDialogVisible: {
    type: Boolean,
    required: true,
  },
});

const emit = defineEmits(["submit", "update:isDialogVisible"]);

const cardDetails = ref(structuredClone(toRaw(props.cardDetails)));

watch(props, () => {
  cardDetails.value = structuredClone(toRaw(props.cardDetails));
});

const formSubmit = () => {
  emit("submit", cardDetails.value);
};
</script>

<template>
  <VDialog
    :width="$vuetify.display.smAndDown ? 'auto' : 600"
    :model-value="props.isDialogVisible"
    @update:model-value="(val) => $emit('update:isDialogVisible', val)"
  >
    <VCard class="pa-5 pa-sm-8">
      <!-- ðŸ‘‰ dialog close btn -->
      <DialogCloseBtn
        variant="text"
        size="small"
        @click="$emit('update:isDialogVisible', false)"
      />

      <!-- ðŸ‘‰ Title -->
      <VCardItem class="text-center">
        <VCardTitle class="text-h5 mb-3">
          {{ props.cardDetails.name ? "Edit Card" : "Add New Card" }}
        </VCardTitle>
        <VCardSubtitle>
          {{
            props.cardDetails.name
              ? "Edit your saved card details"
              : "Add your saved card details"
          }}
        </VCardSubtitle>
      </VCardItem>

      <VCardText class="mt-6">
        <VForm @submit.prevent="() => {}">
          <VRow>
            <!-- ðŸ‘‰ Card Number -->
            <VCol cols="12">
              <VTextField
                v-model="cardDetails.number"
                label="Card Number"
                type="number"
                :rules="[requiredValidator]"
              />
            </VCol>

            <!-- ðŸ‘‰ Card Name -->
            <VCol cols="12" md="6">
              <VTextField
                v-model="cardDetails.name"
                :rules="[requiredValidator, alphaValidator, isNameValid]"
                label="Name"
              />
            </VCol>

            <!-- ðŸ‘‰ Card Expiry -->
            <VCol cols="6" md="3">
              <VTextField
                v-model="cardDetails.expiry"
                :rules="[requiredValidator, expiredateValidator]"
                label="Expiry"
              />
            </VCol>

            <!-- ðŸ‘‰ Card CVV -->
            <VCol cols="6" md="3">
              <VTextField
                v-model="cardDetails.cvv"
                :rules="[requiredValidator]"
                type="number"
                label="CVV"
              />
            </VCol>

            <!-- ðŸ‘‰ Card Primary Set -->
            <VCol cols="12">
              <VSwitch
                v-model="cardDetails.isPrimary"
                label="Save Card for future billing?"
              />
            </VCol>

            <!-- ðŸ‘‰ Card actions -->
            <VCol cols="12" class="d-flex flex-wrap justify-center gap-4">
              <VBtn type="submit" @click="formSubmit"> Submit </VBtn>
              <VBtn
                color="secondary"
                variant="tonal"
                @click="$emit('update:isDialogVisible', false)"
              >
                Cancel
              </VBtn>
            </VCol>
          </VRow>
        </VForm>
      </VCardText>
    </VCard>
  </VDialog>
</template>
